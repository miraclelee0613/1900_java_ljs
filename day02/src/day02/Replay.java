package day02;

public class Replay 
{
	public static void main(String[] args) 
	{
		int num1, num2, num3;
		
		num1 = 12;
		num2 = 26;
		num3 = 49;
		
		double aver;
		
		aver = num1 + num2 + num3;
		aver = aver/3;
		
		System.out.println(num1);
		System.out.println(num2);
		System.out.println(num3);
		System.out.println(aver);
		
		
	}

}
