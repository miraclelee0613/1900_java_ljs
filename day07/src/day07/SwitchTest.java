package day07;

public class SwitchTest {

	public static void main(String[] args) {
		
		int num = 5;
		
		switch(num) {
		case 5:
			System.out.println("5입니다");
			break;
		case 10:
			System.out.println("10입니다");
			break;
		case 1:
			System.out.println("1입니다");
			break;
		default:
			System.out.println("default 입니다.");
//			break;
		}
		

	}

}