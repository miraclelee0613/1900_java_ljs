package day07;

public class SwitchTest {

	public static void main(String[] args) {
		
		int num = 5;
		
		switch(num) {
		case 5:
			System.out.println("5영역임");
			break;
		case 10:
			System.out.println("10영역임");
			break;
		case 1:
			System.out.println("1영역임");
			break;
		default:
			System.out.println("default 영역임");
//			break;
		}
		

	}

}
